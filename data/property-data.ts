/**
 * Get property data by WordPress slug
 */
export function getPropertyDataBySlug(slug: string): PropertyData | null {
  // Normalize slug (remove "- Preview" suffix if present)
  const normalizedSlug = slug
    .replace(/-preview-\d+$/, '')
    .replace(/-preview$/, '')
    .toLowerCase();

  // Try exact match first
  if (PROPERTY_DATA[normalizedSlug]) {
    return PROPERTY_DATA[normalizedSlug];
  }

  // If no exact match, try finding a property that starts with this slug
  // This handles cases like "villa-mandra" → "villa-mandra-mykonos"
  const matchingKey = Object.keys(PROPERTY_DATA).find(key =>
    key.startsWith(normalizedSlug + '-') || key === normalizedSlug
  );

  return matchingKey ? PROPERTY_DATA[matchingKey] : null;
}

/**
 * Extract price number from display price string
 */
export function extractPriceNumber(displayPrice: string): number | null {
  const match = displayPrice.match(/\$([0-9,]+)/);
  if (match) {
    return parseInt(match[1].replace(/,/g, ''), 10);
  }
  return null;
}
